            <h3>COMPOSTAGEM COM MINHOCAS</h3>
            <p>
              <b
                >A compostagem é um processo biológico em que microrganismos
                transformam a matéria orgânica em composto, adubo natural
                semelhante ao solo.
              </b>
            </p>
            <p>
              A compostagem moderna foi disseminada no Ocidente a partir dos
              estudos do agrônomo inglês Albert Howard, considerado o pai da
              agricultura orgânica. No início do século XX, Howard investigou
              por mais de 25 anos as práticas tradicionais realizadas na Índia
              para o enriquecimento natural do solo. Ele percebeu que quando os
              elementos orgânicos se decompõem juntos, formam um subproduto
              riquíssimo em nutrientes (muito mais do que os fertilizantes
              químicos vendidos pelas indústrias). Esta união de elementos é que
              gera o nome “composto”.
            </p>
            <p>
              <b>
                A compostagem com minhocas, também conhecida como
                vermicompostagem, é o processo de transformar restos de
                alimentos e demais resíduos orgânicos em adubo com o auxílio das
                minhocas.
              </b>
            </p>
            <p>
              As composteiras domésticas do Composta São Paulo, produzidas pela
              Morada da Floresta, realizam a compostagem dos resíduos orgânicos
              com a ajuda das minhocas californianas (vermelhas). Elas consomem
              diariamente o equivalente ao seu peso em matéria orgânica,
              duplicam a população a cada 2 meses e diminuem o ritmo de
              reprodução quando percebem que o espaço ficou pequeno para a
              quantidade de indivíduos.
            </p>
            <p>
              O sistema produz adubos de excelente qualidade (composto sólido e
              composto líquido) e, corretamente manuseado, não produz cheiro nem
              atrai animais indesejáveis.
            </p>
